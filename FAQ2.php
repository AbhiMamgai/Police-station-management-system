	<td height="20" colspan="4" valign="middle"><ul>
	  <li>	    An FIR is an account of a cognizable (i.e. over which police has jurisdiction) offence that is entered in a particular format in a register at the police station. Every person has a right to report any matter at the concerned police station and have a case registered in the form of an FIR. The matter may be reported orally or in writing to an officer at a police station or on patrol. An officer who receives an oral report shall reduce it to writing and cause it to be recorded in the FIR register. A certified copy of the FIR, signed by an officer bearing the stamp of the police station is to be provided to the person who reports the crime. </li>
	  <li>No police officer has the authority to refuse registration of a case. In case a crime is reported and a case is not registered, the person who reports the crime must inform the Sub-Divisional Police Officer or the Superintendent of Police or the District Police Officer responsible for the area. For contact details of officers responsible for all jurisdictions in the Punjab click here.</li>
