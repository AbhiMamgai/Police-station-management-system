	  <li><span class="style1" align="justify"> Section 144 of the Code of Criminal Procedure (CrPC) empowers a district government to issue orders in public interest that may place a ban on an activity for a specific period of time extending to two months. Such a ban is enforced by the police who register cases under section 188 of the Pakistan Penal Code for violations of the ban. Section 188 carries a maximum penalty of six months in prison or a fine of up to Rs. 1000 or both. The ban may lifted by the district government at any time or re-imposed after the expiry of the two-month period.</span></li>
